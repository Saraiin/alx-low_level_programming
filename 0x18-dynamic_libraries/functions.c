int add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}
int sub(int a, int b)
{
	int r;

	r = a - b;
	return (r);
}
int mul(int a, int b)
{
	int m;

	m = a * b;
	return (m);
}
int div(int a, int b)
{
	int d;

	d = a / b;
	return (d);
}
int mod(int a, int b)
{
	int md;

	md = a % b;
	return (md);
}
